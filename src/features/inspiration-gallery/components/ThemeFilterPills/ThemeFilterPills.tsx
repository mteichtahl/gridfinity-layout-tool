import { THEME_CONFIG } from '../../types';
import type { InspirationTheme } from '../../types';
import { useTranslation } from '@/i18n';

interface ThemeFilterPillsProps {
  selectedTheme: InspirationTheme | 'all';
  onThemeChange: (theme: InspirationTheme | 'all') => void;
  themeCounts: Record<InspirationTheme | 'all', number>;
}

/**
 * Horizontal scrollable filter pills for theme selection.
 */
export function ThemeFilterPills({
  selectedTheme,
  onThemeChange,
  themeCounts,
}: ThemeFilterPillsProps) {
  const t = useTranslation();
  const themes: Array<InspirationTheme | 'all'> = [
    'all',
    'kitchen',
    'workshop',
    'office',
    'hobby',
    'personal',
  ];

  return (
    <div role="tablist" aria-label={t('gallery.filterByTheme')} className="flex gap-2 min-w-max">
      {themes.map((theme) => {
        const isSelected = selectedTheme === theme;
        const label = theme === 'all' ? t('gallery.all') : THEME_CONFIG[theme].label;
        const count = themeCounts[theme];

        return (
          <button
            type="button"
            key={theme}
            role="tab"
            aria-selected={isSelected}
            onClick={() => onThemeChange(theme)}
            className={`
              flex items-center gap-1.5 px-3 py-1.5 rounded-full text-sm font-medium
              transition-all duration-150 whitespace-nowrap
              ${
                isSelected
                  ? 'bg-accent text-on-dark shadow-sm'
                  : 'bg-surface text-content-secondary hover:text-content hover:bg-surface-hover'
              }
            `}
          >
            {/* Theme icon */}
            {theme !== 'all' && <ThemeIcon theme={theme} className="w-4 h-4" />}
            {theme === 'all' && (
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M4 6a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2H6a2 2 0 01-2-2V6zM14 6a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2h-2a2 2 0 01-2-2V6zM4 16a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2H6a2 2 0 01-2-2v-2zM14 16a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2h-2a2 2 0 01-2-2v-2z"
                />
              </svg>
            )}
            {label}
            {/* Count badge */}
            <span
              className={`
                text-xs px-1.5 py-0.5 rounded-full min-w-[1.25rem] text-center
                ${isSelected ? 'bg-white/20' : 'bg-surface-secondary'}
              `}
            >
              {count}
            </span>
          </button>
        );
      })}
    </div>
  );
}

